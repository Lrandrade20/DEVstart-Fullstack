// Numero + numero = adição
// numero + string ou string + string = concatenação

// Exemplo
let total = 30
let nota =  5
let nome = "Lucas"
total + nota // adicao
20 + total // adicao
"Olá, " + nome // concatenacao

// Não esquecer dos espaços
function cumprimentoUsuario(nome) {
	return "Olá" + nome
}
cumprimentaUsuario("Lucas") // retorna OláLucas (pois faltou o espaço