// alterando itens da matriz
let notas = [5, 10, 8]
notas[0] 
notas[0] = 6
console.log(notas) // retorna [6, 10, 8]

// Alterando itens da matriz com função
function corrigeNotas(notas) {
  notas[0] = 6
  return notas // retorna a matriz com a alteração no item 1
}
corrigeNotas(notas[5, 3, 8]) // 6, 3, 8


